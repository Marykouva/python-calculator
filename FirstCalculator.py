#Divide, Multiply & Subtract figures in python
#Enter first number
num1 = float(input("Enter first number"))
#Enter second number
num2 = float(input("Enter second number"))
#Add the two numbers
AddNum = num1 + num2 
print(f"Sum:{AddNum}")
#Subtract the numbers
SubNum = num1 - num2
print(f"Subtraction:{SubNum}")
#Multiply the numbers
MultiplyNum = num1 * num2
print(f"Multiplication:{MultiplyNum}") 
#Divide the numbers
DivideNum = num1 / num2
print(f"Divide:{DivideNum}")